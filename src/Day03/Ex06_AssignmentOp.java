package Day03;
/*
 [[ Operator ]]
 5. Assignment Operator(대입연산자)
 	=, +=, -=, *=, /=, %=
 */
public class Ex06_AssignmentOp {
	public static void main(String[] args) {
		// = 대입연산자로 변수 num에 10 저장
		int num = 10;
		
		// num = num + 10
		num += 10;
		System.out.println(num);
		
		// num = num - 10
		num -= 10;
		System.out.println(num);
	}

}
